def _system_font_dirs() -> list[Path]:
    """按平台返回候选系统字体目录。"""
    dirs: list[Path] = []
    if sys.platform == "win32":
        dirs.append(Path("C:/Windows/Fonts"))
    elif sys.platform == "darwin":
        dirs.append(Path("/Library/Fonts"))
        dirs.append(Path.home() / "Library/Fonts")
    else:  # Linux 及 Render / Vercel 等部署环境
        for d in [
            "/usr/share/fonts",
            "/usr/local/share/fonts",
            "/usr/share/fonts/truetype",
            "/usr/share/fonts/opentype",
            "/usr/share/fonts/truetype/noto",
            "/usr/share/fonts/truetype/dejavu",
        ]:
            p = Path(d)
            if p.exists():
                dirs.append(p)
        hf = Path.home() / ".fonts"
        if hf.exists():
            dirs.append(hf)
    return dirs


def _find_font(name: str, dirs: list[Path]) -> Path | None:
    """在 dirs 中递归搜索指定文件名。"""
    for d in dirs:
        if not d.exists():
            continue
        for f in d.rglob(name):
            if f.suffix.lower() in (".ttf", ".ttc", ".otf"):
                return f
    return None


def _discover_font_dirs() -> list[Path]:
    """定位 fonts/ 目录。

    Vercel 等 serverless 平台上 __file__ 所在目录不一定和项目根一致，
    所以同时尝试多个候选位置，并把当前工作目录也纳入搜索。
    """
    candidates: list[Path] = []
    # 1. 与 app.py 同级的 fonts/
    candidates.append(Path(__file__).parent / "fonts")
    # 2. 当前工作目录下的 fonts/（Vercel 上 CWD 通常是项目根）
    candidates.append(Path.cwd() / "fonts")
    # 3. 从 CWD 向上找 3 级，看有没有 fonts/
    p = Path.cwd()
    for _ in range(3):
        candidates.append(p / "fonts")
        p = p.parent
    # 去重并保持顺序
    seen: set[Path] = set()
    result: list[Path] = []
    for c in candidates:
        try:
            resolved = c.resolve()
        except Exception:
            resolved = c
        if resolved not in seen:
            seen.add(resolved)
            result.append(c)
    return result



# ---------------------------------------------------------------------------
# 累进字形: 每一步显示"写到目前为止的字形"
#   规则: 对每一步的笔画前缀, 反查哪个已知字正好等于这个前缀;
#         命中则显示该字，未命中（独立不成字的前缀）则显示目标整字。
#   这样所有 1000+ 字自动覆盖，无需额外数据:
#     二 → (横)→一     (横,横)→二              → 一 二
#     三 → (横)→一     (横,横)→二 (横,横,横)→三 → 一 二 三
#     儿 → (撇)→?→儿  (撇,竖弯钩)→儿           → 丿 儿 ✓
#     人 → (撇)→?→人  (撇,捺)→人               → 人 人
#     木 → (横)→一    (横,竖)→十  (横,竖,撇)→?→木  (横,竖,撇,捺)→木 → 一 十 木 木
# ---------------------------------------------------------------------------

# 已知字的完整笔画列表索引: tuple(strokes) → 代表字
_STROKE_SEQ_INDEX: dict[tuple[str, ...], str] = {}
for _c, _sl in _STROKE_NAMES.items():
    _STROKE_SEQ_INDEX.setdefault(tuple(_sl), _c)

# 单笔 → 字形（仅 5 种基本笔画有独立的汉字/笔画符号）
_BASE_STROKE_GLYPH: dict[str, str] = {
    "横": "一", "竖": "丨", "撇": "丿", "点": "丶", "捺": "㇏", "提": "㇀",
}


def get_progressive_forms(ch: str) -> list[str]:
    """返回每写一步时呈现的字形列表（用于栏内累进显示）。

    优先级:
      1. 前缀笔画序列与某个已知字相同 → 显示该字的字形 (最直观)
      2. 单笔画有独立汉字/笔画符号 → 显示该符号 (撇→丿, 横→一, …)
      3. 否则 → 显示整字本身 ("整字重复"兜底)
    """
    if ch not in _STROKE_NAMES:
        return get_stroke_names(ch)
    names = _STROKE_NAMES[ch]
    full = tuple(names)
    out: list[str] = []
    for i in range(1, len(names) + 1):
        prefix = tuple(names[:i])
        if prefix == full:
            out.append(ch)                        # 末笔 → 始终显示目标字本身
        else:
            partial = _STROKE_SEQ_INDEX.get(prefix)
            if partial is not None:
                out.append(partial)               # 命中: 显示部分字
            elif len(prefix) == 1:
                out.append(_BASE_STROKE_GLYPH.get(prefix[0], ch))  # 单笔 → 字形
            else:
                out.append(ch)                    # 兜底: 显示整字
    return out


def get_stroke_names(ch: str) -> list[str]:
    """返回笔顺名列表；未知字返回 ①②③ 占位。"""
    if ch in _STROKE_NAMES:
        return _STROKE_NAMES[ch]
    n = get_stroke_count(ch)
    if n <= 0:
        return ["？"]
    return [f"①", f"②", f"③", f"④", f"⑤", f"⑥", f"⑦", f"⑧", f"⑨", f"⑩"][:n] \
        if n <= 10 else [str(i + 1) for i in range(n)]



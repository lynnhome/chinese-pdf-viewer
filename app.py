def _download_noto(dest: Path) -> bool:
    """下载 Noto Sans SC 到 dest。失败返回 False。"""
    dest.parent.mkdir(parents=True, exist_ok=True)
    for url in _FONT_URLS:
        try:
            print(f"[font] downloading from {url} ...")
            urllib.request.urlretrieve(url, dest)
            size = dest.stat().st_size
            print(f"[font] downloaded {size} bytes")
            if size > 100_000:   # >100KB 才认为有效
                return True
        except Exception as e:
            print(f"[font] download failed: {e}")
            continue
    return False


def register_fonts() -> str:
    """尝试注册一个 CJK 字体。

    优先级:
      1. 项目本地 / 系统目录中的字体文件
      2. /tmp/ 缓存的 Noto (Vercel 可写目录)
      3. 从 CDN 下载到 /tmp/ 后注册
      4. Helvetica 兜底
    """
    # --- 第 1 步: 搜索本地/系统字体 ---
    for fname, sub in _FONT_CANDIDATES:
        fp = _find_font(fname, _FONT_DIRS)
        if fp is None:
            continue
        # 跳过明显不是真实字体的文件 (<1KB 说明是占位符/损坏)
        try:
            if fp.stat().st_size < 1000:
                print(f"[font] skip {fp} (only {fp.stat().st_size} bytes, probably placeholder)")
                continue
        except Exception:
            continue
        try:
            if sub is not None:
                pdfmetrics.registerFont(TTFont("CJK", str(fp), subfontIndex=sub))
            else:
                pdfmetrics.registerFont(TTFont("CJK", str(fp)))
            print(f"[font] registered: {fp}")
            return "CJK"
        except Exception as e:
            print(f"[font] candidate {fname} at {fp} failed: {e}")
            continue

    # --- 第 2 步: 使用 /tmp/ 缓存或下载新字体 ---
    # Vercel 的 /tmp 可写 (500MB), 冷启动之间持久化
    tmp_font = Path("/tmp") / "MaShanZheng-Regular.ttf"
    if tmp_font.exists() and tmp_font.stat().st_size > 100_000:
        try:
            pdfmetrics.registerFont(TTFont("CJK", str(tmp_font)))
            print(f"[font] registered from cache: {tmp_font}")
            return "CJK"
        except Exception as e:
            print(f"[font] cached font failed: {e}")

    # 下载到 /tmp/
    if not tmp_font.exists() or tmp_font.stat().st_size < 100_000:
        print("[font] downloading Ma Shan Zheng (楷体) to /tmp/ ...")
        _download_noto(tmp_font)

    if tmp_font.exists() and tmp_font.stat().st_size > 100_000:
        try:
            pdfmetrics.registerFont(TTFont("CJK", str(tmp_font)))
            print(f"[font] registered (downloaded): {tmp_font}")
            return "CJK"
        except Exception as e:
            print(f"[font] downloaded font failed: {e}")

    print("[font] WARNING: no CJK font available, falling back to Helvetica")
    return "Helvetica"



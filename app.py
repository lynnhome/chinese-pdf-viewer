BLOCK_H     = CELLS_COL * CELL   # 54 mm


def draw_tianzi(c, x, y, size, char=None, char_color=None, show_mizi=True):
    """画一个田字格（外框+十字虚线+可选米字虚线+可选字符）。"""
    c.setFillColor(COLOR_BG)
    c.rect(x, y, size, size, fill=1, stroke=0)

    c.setStrokeColor(COLOR_GRID)
    c.setLineWidth(0.5)
    c.rect(x, y, size, size, fill=0, stroke=1)

    c.setDash(2, 2)
    c.setStrokeColor(COLOR_GUIDE)
    c.setLineWidth(0.3)
    c.line(x, y + size / 2, x + size, y + size / 2)
    c.line(x + size / 2, y, x + size / 2, y + size)
    if show_mizi:
        c.setDash(1, 2)
        c.setLineWidth(0.25)
        c.line(x, y, x + size, y + size)
        c.line(x + size, y, x, y + size)
    c.setDash()

    if char:
        c.setFillColor(char_color or COLOR_CHAR)
        c.setFont(FONT_NAME, size * 0.72)
        ascent = size * 0.72
        cy = y + size / 2 - ascent * 0.30
        c.drawCentredString(x + size / 2, cy, char)


def draw_char_block(c, x, y, char, stroke_count, stroke_names):
    """
    在 (x, y) 处画一个完整的描红区块。
    左栏: 笔画数(大) + 笔顺名(小)
    右侧: 3 行 × (3 描红 + 3 自写)
    """
    # 区块背景
    c.setFillColor(HexColor("#FFFBF5"))
    c.roundRect(x, y, BLOCK_W, BLOCK_H, 3 * mm, fill=1, stroke=0)
    c.setStrokeColor(COLOR_BORDER)
    c.setLineWidth(0.4)
    c.roundRect(x, y, BLOCK_W, BLOCK_H, 3 * mm, fill=0, stroke=1)

    # ---- 右侧网格 ----
    # 10 格一组: 2描红 + 2自写 + 2描红 + 2自写 + 1描红 + 1自写
    # 第 0,1,4,5,8 列描红；第 2,3,6,7,9 列自写
    TRACE_COLS = {0, 1, 4, 5, 8}
    gx = x
    gy = y
    for row in range(CELLS_COL):
        for col in range(CELLS_ROW):
            cx = gx + col * CELL
            cy = gy + (CELLS_COL - 1 - row) * CELL
            if col in TRACE_COLS:
                draw_tianzi(c, cx, cy, CELL, char, COLOR_CHAR, show_mizi=True)
            else:
                draw_tianzi(c, cx, cy, CELL, None, None, show_mizi=True)


def generate_pdf(text: str) -> io.BytesIO:
    """生成描红练习 PDF。"""
    chars = [ch for ch in text.strip() if ch.strip()]
    if not chars:
        chars = [" "]

    buf = io.BytesIO()
    c = canvas.Canvas(buf, pagesize=A4)
    c.setTitle("汉字描红练习")

    page_w, page_h = A4
    usable_w = page_w - 2 * MARGIN_X
    usable_h = page_h - MARGIN_TOP - MARGIN_BOT

    # 每行放几个字
    per_row = max(1, int((usable_w + BLOCK_GAP) / (BLOCK_W + BLOCK_GAP)))
    # 每列放几个字
    per_col = max(1, int((usable_h + BLOCK_GAP) / (BLOCK_H + BLOCK_GAP)))
    per_page = per_row * per_col

    idx = 0
    while idx < len(chars):
        # 标题
        c.setFillColor(COLOR_TEXT)
        c.setFont(FONT_NAME, 15)
        c.drawCentredString(page_w / 2, page_h - 9 * mm, "汉字描红练习")
        c.setFont(FONT_NAME, 8)
        c.setFillColor(COLOR_LABEL)
        c.drawCentredString(page_w / 2, page_h - 13 * mm,
                            "描红 · 自写 交替练习")

        for i in range(per_page):
            if idx >= len(chars):
                break
            row = i // per_row
            col = i % per_row
            bx = MARGIN_X + col * (BLOCK_W + BLOCK_GAP)
            by = page_h - MARGIN_TOP - 8 * mm - (row + 1) * (BLOCK_H + BLOCK_GAP)
            ch = chars[idx]
            draw_char_block(c, bx, by, ch, get_stroke_count(ch), get_progressive_forms(ch))
            idx += 1

        c.showPage()

    c.save()
    buf.seek(0)
    return buf



UNIHAN_CACHE = Path.home() / ".hanzipy_data" / "Unihan_IRGSources.txt"
_STROKES: dict[str, int] = {}


def _load_strokes():
    if not UNIHAN_CACHE.exists():
        return
    with open(UNIHAN_CACHE, encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line or line.startswith("#"):
                continue
            parts = line.split("\t")
            if len(parts) >= 3 and parts[1] == "kTotalStrokes":
                try:
                    cp = int(parts[0][2:], 16)
                    _STROKES[chr(cp)] = int(parts[2])
                except (ValueError, OverflowError):
                    continue


_load_strokes()


def get_stroke_count(ch: str) -> int:
    """返回单字笔画数，未知返回 0。"""
    return _STROKES.get(ch, 0)


